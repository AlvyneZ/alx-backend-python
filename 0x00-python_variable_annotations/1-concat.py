#!/usr/bin/env python3
"""
1-concat.py - Provides a method for adding two strings
 to concatenate them
"""


def concat(str1: str, str2: str) -> str:
    """
    Adds two strings to concatenate

    str1 (str): first string
    str2 (str): second string

    Returns: the concatenation
    """
    return str1 + str2
